<script type="text/javascript">
    $('#search').on('input', function(){
        var keyword = $(this).val();
        var url = "https://www.bisitailigan.com/search?keyword=" + keyword;
        var el = $('#search-autosuggest');
        el.empty();
        el.show();
        $.getJSON(url, function(data){

            if(data.length <=0 ){
                el.append('<a href="#!" class="autosuggest-item text-danger">No results found</a>');
            }else{
               $.each(data, function(i, item){
                   el.append('<a href="../../listing/'+item['slug']+'" class="autosuggest-item">'+item['title']+'</a>');
               }); 
            }
            
        });
    });
</script>



                    